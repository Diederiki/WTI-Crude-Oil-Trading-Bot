-- =============================================================================
-- WTI Trading Bot - Initial Database Schema
-- =============================================================================
-- This script creates the core database schema for the WTI trading bot.
-- It includes tables for market data, signals, orders, positions, and risk events.
-- =============================================================================

-- Enable required extensions
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS "pg_stat_statements";

-- Try to enable TimescaleDB if available (optional)
DO $$
BEGIN
    CREATE EXTENSION IF NOT EXISTS "timescaledb";
EXCEPTION WHEN OTHERS THEN
    RAISE NOTICE 'TimescaleDB not available, using standard PostgreSQL';
END $$;

-- =============================================================================
-- Core Tables
-- =============================================================================

-- -----------------------------------------------------------------------------
-- Market Data Tables
-- -----------------------------------------------------------------------------

-- Raw tick data (price updates)
CREATE TABLE IF NOT EXISTS ticks (
    id BIGSERIAL PRIMARY KEY,
    symbol VARCHAR(20) NOT NULL,
    timestamp TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    bid_price DECIMAL(18, 8),
    ask_price DECIMAL(18, 8),
    last_price DECIMAL(18, 8) NOT NULL,
    bid_size BIGINT,
    ask_size BIGINT,
    last_size BIGINT,
    volume BIGINT,
    exchange VARCHAR(20),
    feed_source VARCHAR(50) NOT NULL,
    received_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    processed_at TIMESTAMPTZ,
    is_stale BOOLEAN DEFAULT FALSE,
    anomaly_flags INTEGER DEFAULT 0,
    raw_data JSONB,
    
    CONSTRAINT ticks_price_check CHECK (last_price > 0)
);

-- Create hypertable if TimescaleDB is available
DO $$
BEGIN
    PERFORM create_hypertable('ticks', 'timestamp', if_not_exists => TRUE, chunk_time_interval => INTERVAL '1 day');
EXCEPTION WHEN OTHERS THEN
    RAISE NOTICE 'Could not create hypertable for ticks';
END $$;

-- Create indexes for ticks
CREATE INDEX IF NOT EXISTS idx_ticks_symbol_timestamp ON ticks (symbol, timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_ticks_timestamp ON ticks (timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_ticks_feed_source ON ticks (feed_source);
CREATE INDEX IF NOT EXISTS idx_ticks_received_at ON ticks (received_at DESC);

-- OHLCV bars (candlestick data)
CREATE TABLE IF NOT EXISTS bars (
    id BIGSERIAL PRIMARY KEY,
    symbol VARCHAR(20) NOT NULL,
    timestamp TIMESTAMPTZ NOT NULL,
    interval_seconds INTEGER NOT NULL DEFAULT 60,
    open DECIMAL(18, 8) NOT NULL,
    high DECIMAL(18, 8) NOT NULL,
    low DECIMAL(18, 8) NOT NULL,
    close DECIMAL(18, 8) NOT NULL,
    volume BIGINT NOT NULL DEFAULT 0,
    vwap DECIMAL(18, 8),
    trades_count INTEGER,
    source VARCHAR(20) DEFAULT 'aggregated',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    
    CONSTRAINT bars_price_check CHECK (high >= low AND high >= open AND high >= close),
    CONSTRAINT bars_volume_check CHECK (volume >= 0),
    CONSTRAINT bars_interval_check CHECK (interval_seconds > 0),
    UNIQUE (symbol, timestamp, interval_seconds)
);

-- Create hypertable if TimescaleDB is available
DO $$
BEGIN
    PERFORM create_hypertable('bars', 'timestamp', if_not_exists => TRUE, chunk_time_interval => INTERVAL '7 days');
EXCEPTION WHEN OTHERS THEN
    RAISE NOTICE 'Could not create hypertable for bars';
END $$;

-- Create indexes for bars
CREATE INDEX IF NOT EXISTS idx_bars_symbol_timestamp ON bars (symbol, timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_bars_interval ON bars (interval_seconds);

-- -----------------------------------------------------------------------------
-- Trading Signal Tables
-- -----------------------------------------------------------------------------

-- Trading signals generated by strategies
CREATE TABLE IF NOT EXISTS signals (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    symbol VARCHAR(20) NOT NULL,
    timestamp TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    signal_type VARCHAR(20) NOT NULL CHECK (signal_type IN ('BUY', 'SELL', 'HOLD', 'EXIT')),
    strategy_name VARCHAR(100) NOT NULL,
    strategy_version VARCHAR(20),
    confidence DECIMAL(5, 4) CHECK (confidence >= 0 AND confidence <= 1),
    price_at_signal DECIMAL(18, 8),
    expected_entry DECIMAL(18, 8),
    expected_exit DECIMAL(18, 8),
    stop_loss DECIMAL(18, 8),
    take_profit DECIMAL(18, 8),
    position_size INTEGER,
    time_in_force VARCHAR(20) DEFAULT 'GTC',
    metadata JSONB,
    status VARCHAR(20) DEFAULT 'pending' CHECK (status IN ('pending', 'executed', 'expired', 'cancelled')),
    executed_at TIMESTAMPTZ,
    order_id UUID,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_signals_symbol_timestamp ON signals (symbol, timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_signals_strategy ON signals (strategy_name, timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_signals_status ON signals (status);
CREATE INDEX IF NOT EXISTS idx_signals_signal_type ON signals (signal_type);

-- -----------------------------------------------------------------------------
-- Order Management Tables
-- -----------------------------------------------------------------------------

-- Orders placed by the system
CREATE TABLE IF NOT EXISTS orders (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    external_order_id VARCHAR(100),
    symbol VARCHAR(20) NOT NULL,
    side VARCHAR(10) NOT NULL CHECK (side IN ('BUY', 'SELL')),
    order_type VARCHAR(20) NOT NULL CHECK (order_type IN ('MARKET', 'LIMIT', 'STOP', 'STOP_LIMIT', 'TRAILING_STOP')),
    quantity INTEGER NOT NULL CHECK (quantity > 0),
    filled_quantity INTEGER NOT NULL DEFAULT 0 CHECK (filled_quantity >= 0),
    remaining_quantity INTEGER GENERATED ALWAYS AS (quantity - filled_quantity) STORED,
    price DECIMAL(18, 8),
    stop_price DECIMAL(18, 8),
    time_in_force VARCHAR(10) DEFAULT 'GTC' CHECK (time_in_force IN ('GTC', 'IOC', 'FOK', 'DAY', 'GTD')),
    status VARCHAR(20) NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'submitted', 'partially_filled', 'filled', 'cancelled', 'rejected', 'expired')),
    exchange VARCHAR(20),
    account_id VARCHAR(50),
    signal_id UUID REFERENCES signals(id) ON DELETE SET NULL,
    parent_order_id UUID REFERENCES orders(id) ON DELETE SET NULL,
    is_oco BOOLEAN DEFAULT FALSE,
    oco_group_id UUID,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    submitted_at TIMESTAMPTZ,
    filled_at TIMESTAMPTZ,
    cancelled_at TIMESTAMPTZ,
    rejected_at TIMESTAMPTZ,
    reject_reason TEXT,
    expires_at TIMESTAMPTZ,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    metadata JSONB
);

CREATE INDEX IF NOT EXISTS idx_orders_symbol_status ON orders (symbol, status);
CREATE INDEX IF NOT EXISTS idx_orders_status ON orders (status);
CREATE INDEX IF NOT EXISTS idx_orders_external_id ON orders (external_order_id);
CREATE INDEX IF NOT EXISTS idx_orders_signal_id ON orders (signal_id);
CREATE INDEX IF NOT EXISTS idx_orders_created_at ON orders (created_at DESC);

-- Order fills/executions
CREATE TABLE IF NOT EXISTS fills (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    order_id UUID NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    external_fill_id VARCHAR(100),
    symbol VARCHAR(20) NOT NULL,
    side VARCHAR(10) NOT NULL,
    quantity INTEGER NOT NULL CHECK (quantity > 0),
    price DECIMAL(18, 8) NOT NULL,
    commission DECIMAL(18, 8) DEFAULT 0,
    commission_currency VARCHAR(10) DEFAULT 'USD',
    exchange VARCHAR(20),
    liquidity VARCHAR(10) CHECK (liquidity IN ('maker', 'taker')),
    timestamp TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    metadata JSONB
);

CREATE INDEX IF NOT EXISTS idx_fills_order_id ON fills (order_id);
CREATE INDEX IF NOT EXISTS idx_fills_symbol_timestamp ON fills (symbol, timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_fills_timestamp ON fills (timestamp DESC);

-- -----------------------------------------------------------------------------
-- Position Management Tables
-- -----------------------------------------------------------------------------

-- Current positions
CREATE TABLE IF NOT EXISTS positions (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    symbol VARCHAR(20) NOT NULL UNIQUE,
    side VARCHAR(10) NOT NULL CHECK (side IN ('LONG', 'SHORT', 'FLAT')),
    quantity INTEGER NOT NULL DEFAULT 0,
    average_entry_price DECIMAL(18, 8),
    current_price DECIMAL(18, 8),
    unrealized_pnl DECIMAL(18, 2) DEFAULT 0,
    realized_pnl DECIMAL(18, 2) DEFAULT 0,
    total_pnl DECIMAL(18, 2) GENERATED ALWAYS AS (unrealized_pnl + realized_pnl) STORED,
    margin_used DECIMAL(18, 2) DEFAULT 0,
    opened_at TIMESTAMPTZ,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    last_fill_id UUID REFERENCES fills(id),
    metadata JSONB
);

CREATE INDEX IF NOT EXISTS idx_positions_symbol ON positions (symbol);
CREATE INDEX IF NOT EXISTS idx_positions_side ON positions (side);

-- Position history (closed positions)
CREATE TABLE IF NOT EXISTS position_history (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    symbol VARCHAR(20) NOT NULL,
    side VARCHAR(10) NOT NULL,
    entry_quantity INTEGER NOT NULL,
    exit_quantity INTEGER NOT NULL,
    average_entry_price DECIMAL(18, 8) NOT NULL,
    average_exit_price DECIMAL(18, 8) NOT NULL,
    realized_pnl DECIMAL(18, 2) NOT NULL,
    commission_paid DECIMAL(18, 2) DEFAULT 0,
    opened_at TIMESTAMPTZ NOT NULL,
    closed_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    duration_seconds INTEGER GENERATED ALWAYS AS (EXTRACT(EPOCH FROM (closed_at - opened_at))) STORED,
    entry_order_ids UUID[],
    exit_order_ids UUID[],
    metadata JSONB
);

CREATE INDEX IF NOT EXISTS idx_position_history_symbol ON position_history (symbol);
CREATE INDEX IF NOT EXISTS idx_position_history_closed_at ON position_history (closed_at DESC);

