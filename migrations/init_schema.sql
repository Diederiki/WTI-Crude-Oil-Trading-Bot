-- Update timestamp trigger function
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Apply update trigger to tables with updated_at column
DO $$
DECLARE
    tbl RECORD;
BEGIN
    FOR tbl IN 
        SELECT table_name 
        FROM information_schema.columns 
        WHERE column_name = 'updated_at' 
        AND table_schema = 'public'
    LOOP
        EXECUTE format('DROP TRIGGER IF EXISTS update_%I_updated_at ON %I', tbl.table_name, tbl.table_name);
        EXECUTE format('
            CREATE TRIGGER update_%I_updated_at
            BEFORE UPDATE ON %I
            FOR EACH ROW
            EXECUTE FUNCTION update_updated_at_column()
        ', tbl.table_name, tbl.table_name);
    END LOOP;
END $$;

-- Function to calculate position P&L
CREATE OR REPLACE FUNCTION calculate_position_pnl(
    p_symbol VARCHAR(20),
    p_current_price DECIMAL(18, 8)
) RETURNS TABLE (
    unrealized_pnl DECIMAL(18, 2),
    realized_pnl DECIMAL(18, 2),
    total_pnl DECIMAL(18, 2)
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        CASE 
            WHEN p.side = 'LONG' THEN (p_current_price - p.average_entry_price) * p.quantity
            WHEN p.side = 'SHORT' THEN (p.average_entry_price - p_current_price) * p.quantity
            ELSE 0
        END as unrealized_pnl,
        p.realized_pnl,
        CASE 
            WHEN p.side = 'LONG' THEN (p_current_price - p.average_entry_price) * p.quantity + p.realized_pnl
            WHEN p.side = 'SHORT' THEN (p.average_entry_price - p_current_price) * p.quantity + p.realized_pnl
            ELSE p.realized_pnl
        END as total_pnl
    FROM positions p
    WHERE p.symbol = p_symbol;
END;
$$ LANGUAGE plpgsql;

