-- Daily P&L tracking for risk management
CREATE TABLE IF NOT EXISTS daily_pnl (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    date DATE NOT NULL UNIQUE,
    starting_equity DECIMAL(18, 2) NOT NULL,
    ending_equity DECIMAL(18, 2),
    realized_pnl DECIMAL(18, 2) DEFAULT 0,
    unrealized_pnl DECIMAL(18, 2) DEFAULT 0,
    commission_paid DECIMAL(18, 2) DEFAULT 0,
    total_pnl DECIMAL(18, 2) GENERATED ALWAYS AS (realized_pnl + unrealized_pnl - commission_paid) STORED,
    total_trades INTEGER DEFAULT 0,
    winning_trades INTEGER DEFAULT 0,
    losing_trades INTEGER DEFAULT 0,
    max_drawdown_pct DECIMAL(8, 4),
    max_drawdown_amount DECIMAL(18, 2),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
