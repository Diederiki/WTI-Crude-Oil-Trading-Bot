    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    event_type VARCHAR(50) NOT NULL CHECK (event_type IN ('position_limit', 'daily_loss', 'drawdown', 'order_rate', 'margin_call', 'kill_switch', 'manual_override')),
    severity VARCHAR(20) NOT NULL CHECK (severity IN ('info', 'warning', 'critical', 'emergency')),
    symbol VARCHAR(20),
    description TEXT NOT NULL,
    triggered_value DECIMAL(18, 8),
    limit_value DECIMAL(18, 8),
    action_taken VARCHAR(100),
    acknowledged BOOLEAN DEFAULT FALSE,
    acknowledged_by VARCHAR(100),
    acknowledged_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
