-- =============================================================================
-- Views
-- =============================================================================

-- Current position summary view
CREATE OR REPLACE VIEW position_summary AS
SELECT 
    symbol,
    side,
    quantity,
    average_entry_price,
    current_price,
    unrealized_pnl,
    realized_pnl,
    total_pnl,
    ROUND((current_price - average_entry_price) / NULLIF(average_entry_price, 0) * 100, 4) as pnl_pct,
    updated_at
FROM positions
WHERE side != 'FLAT';

-- Open orders view
CREATE OR REPLACE VIEW open_orders AS
SELECT 
    o.*,
    s.signal_type as parent_signal_type,
    s.confidence as parent_signal_confidence
FROM orders o
LEFT JOIN signals s ON o.signal_id = s.id
WHERE o.status IN ('pending', 'submitted', 'partially_filled');

-- Daily trading summary view
CREATE OR REPLACE VIEW daily_trading_summary AS
SELECT 
    date,
    starting_equity,
    ending_equity,
    total_pnl,
    total_trades,
    winning_trades,
    losing_trades,
    CASE 
        WHEN total_trades > 0 THEN ROUND(winning_trades::numeric / total_trades * 100, 2)
        ELSE 0 
    END as win_rate_pct,
    max_drawdown_pct,
    max_drawdown_amount
FROM daily_pnl
ORDER BY date DESC;

